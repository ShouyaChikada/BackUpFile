CLifeManager* CLifeManager::m_pInstance = nullptr; // インスタンス変数

//=================================================
// コンストラクタ
//=================================================
CLifeManager::CLifeManager() : m_pLife{}
{
	
}

//=================================================
// デストラクタ
//=================================================
CLifeManager::~CLifeManager()
{

}

//=================================================
// 初期化
//=================================================
HRESULT CLifeManager::Init(void)
{
	for (int nCnt = 0; nCnt < MAX_LIFE; nCnt++)
	{
		m_pLife[nCnt] = CLife::Create(D3DXVECTOR3(1200.0f - (80.0f * nCnt), 670.0f, 0.0f));
	}

	return S_OK;
}

//=================================================
// 破棄
//=================================================
void CLifeManager::Uninit(void)
{
	// シングルトンの破棄
	if (m_pInstance)
	{
		delete m_pInstance;
		m_pInstance = nullptr;
	}
}

