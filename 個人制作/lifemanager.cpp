//=================================================
// インスタンス取得処理
//=================================================
CLifeManager* CLifeManager::Instance(void)
{
	// nullチェック
	if (m_pInstance == nullptr)
	{
		// インスタンス生成
		m_pInstance = new CLifeManager;
	}

	// 生成されたインスタンスを返す
	return m_pInstance;
}